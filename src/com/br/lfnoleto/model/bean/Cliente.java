package com.br.lfnoleto.model.bean;

public class Cliente {

	private int idclient;
	private String nome;
	private String cpf;
	private String cep;
	private String cidade;
	private String uf;
	private String endclient;
	private String foneclient;
	private String emailclient;
	
	public int getIdclient() {
		return idclient;
	}
	public void setIdclient(int idclient) {
		this.idclient = idclient;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getUf() {
		return uf;
	}
	public void setUf(String uf) {
		this.uf = uf;
	}
	public String getEndclient() {
		return endclient;
	}
	public void setEndclient(String endclient) {
		this.endclient = endclient;
	}
	public String getFoneclient() {
		return foneclient;
	}
	public void setFoneclient(String foneclient) {
		this.foneclient = foneclient;
	}
	public String getEmailclient() {
		return emailclient;
	}
	public void setEmailclient(String emailclient) {
		this.emailclient = emailclient;
	}
	
	
	
	
}
